

//function declaration
function soma(x, y) {
    return x + y
}

console.log(soma(3, 4))

//function expression
const sub = function (x, y) {
    return x - y
}

console.log(sub(7, 4))

//named function expression - geralmente, utilizada para debug.
const mult = function mult(x, y) {
    return x * y
}
console.log(mult(2, 10))

